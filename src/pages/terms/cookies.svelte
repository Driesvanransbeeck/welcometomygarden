<h2>Cookie policy</h2>
<h3>1. What Are Cookies</h3>
<p>
  As is common practice with almost all professional websites, this site uses cookies. Cookies are
  tiny files that are downloaded to your computer, to improve your experience. This page describes
  what information they gather, how we use it and why we sometimes need to store these cookies. We
  will also share how you can prevent these cookies from being stored. However, this may downgrade
  or 'break' certain elements of the site functionality. For more general information on cookies
  <a
    href="https://www.google.com/url?q=https://en.wikipedia.org/wiki/HTTP_cookie&amp;sa=D&amp;ust=1592214262286000">
    see this Wikipedia article on HTTP Cookies.
  </a>
</p>
<h3>2. How We Use Cookies</h3>
<p>
  We use cookies for a variety of reasons detailed below. Unfortunately, in most cases there are no
  industry standard options for disabling cookies without completely disabling the functionality and
  features they add to this site. It is recommended that you leave all cookies if you are not sure
  whether you need them or not in case they are used to provide a service that you use.
</p>
<h3>3. Disabling Cookies</h3>
<p>
  You can prevent cookies being downloaded by adjusting your browser settings (see the Help menu of
  your browser to find out how to do this). Please be aware that disabling cookies might affect the
  functionality of this and many other websites that you visit. Disabling cookies will usually
  result in disabling certain functionality and features of this site as well. If you'd like to
  disable cookies, we recommend using
  <a
    href="https://www.google.com/url?q=https://addons.mozilla.org/en-US/firefox/addon/privacy-badger17/&amp;sa=D&amp;ust=1592214262287000">
    Privacy Badger
  </a>
  on Firefox.
</p>
<h3>4. The Cookies We Set</h3>
<p>
  If you create an account with us then we will use cookies for the management of the signup process
  and general administration. These cookies will usually be deleted when you log out. However, in
  some cases they may remain afterwards to remember your site preferences when logged out.
</p>
<p>
  We use cookies when you are logged in so that we can remember this fact. This prevents you from
  having to log in every single time you visit a new page. These cookies are typically removed or
  cleared when you log out to ensure that you can only access restricted features and areas when
  logged in.
</p>
<p>
  This site might offer newsletter or email subscription services in the future and cookies may be
  used to remember if you are already registered and whether to show certain notifications which
  might only be valid to subscribed/unsubscribed users.
</p>
<p>
  When you submit data through a form such as those found on contact pages or comment forms, cookies
  may be set to remember your user details for future correspondence.
</p>
<p>
  In order to provide you with a great experience on this site, we provide the functionality to set
  your preferences for how this site runs when you use it. In order to remember your preferences we
  need to set cookies, so that this information can be recalled whenever you interact with a page
  that is affected by your preferences.
</p>
